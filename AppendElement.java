import java.util.HashSet;

public class AppendElement {

	public static void main(String[] args) {
	
		HashSet<String> hs=new HashSet<String>();
		
		hs.add("Red");
		hs.add("white");
		hs.add("Pink");
		hs.add("Yellow");
		hs.add("Black");
		hs.add("Green");
		
		System.out.println("The Hash Set: "+hs);

	}

}
//Sample Output:The Hash Set: [Red, White, Pink, Yellow, Black, Green]